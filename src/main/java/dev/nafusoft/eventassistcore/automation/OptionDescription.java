package dev.nafusoft.eventassistcore.automation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation can be used in {@link ActionOptions} implementation classes to provide descriptions of options to other plugins that use the data.
 */
@Target({ElementType.RECORD_COMPONENT})
@Retention(RetentionPolicy.RUNTIME)
public @interface OptionDescription {
    String value();
}
